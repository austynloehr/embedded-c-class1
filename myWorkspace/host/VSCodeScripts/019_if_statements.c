#include <stdio.h>
#include <stdint.h>

int main(){
    /*If statements will execute if expression evaluation is non-zero*/
    /*Do not use semi-colon after expression*/
    if (10){
        printf("This works\n");
    }

    if (1 > 0){
        printf("This also works\n\n");
    }

    return 0;
}